// ── Excel helpers ────────────────────────────────────────────────────────────

function applyHeaderRow(ws, headers, rowNum = 1) {
  const row = ws.getRow(rowNum);
  headers.forEach((h, i) => {
    const cell = row.getCell(i + 1);
    cell.value = h;
    cell.font = { bold: true, color: { argb: 'FFFFFFFF' }, size: 11 };
    cell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: `FF${BRAND.primary}` } };
    cell.border = {
      bottom: { style: 'thin', color: { argb: 'FFD0D0D0' } },
    };
    cell.alignment = { vertical: 'middle', horizontal: 'center', wrapText: true };
  });
  row.height = 20;
  ws.autoFilter = { from: { row: rowNum, column: 1 }, to: { row: rowNum, column: headers.length } };
}

function applyBrandHeader(ws, reportName, period) {
  ws.mergeCells('A1:H1');
  const titleCell = ws.getCell('A1');
  titleCell.value = BRAND.name;
  titleCell.font = { bold: true, size: 16, color: { argb: `FF${BRAND.primary}` } };
  titleCell.alignment = { horizontal: 'center' };

  ws.mergeCells('A2:H2');
  const subCell = ws.getCell('A2');
  subCell.value = `${reportName} — ${period}`;
  subCell.font = { size: 12, color: { argb: 'FF555555' } };
  subCell.alignment = { horizontal: 'center' };

  ws.getRow(3).height = 8;
}

function autoFitColumns(ws) {
  ws.columns.forEach((col) => {
    let maxLen = 10;
    col.eachCell({ includeEmpty: false }, (cell) => {
      const len = cell.value ? String(cell.value).length : 0;
      if (len > maxLen) maxLen = len;
    });
    col.width = Math.min(maxLen + 4, 50);
  });
}

function addFooter(ws, userName) {
  const lastRow = ws.lastRow?.number + 2 || ws.rowCount + 2;
  const footerCell = ws.getCell(`A${lastRow}`);
  footerCell.value = `Generated by ${userName} on ${new Date().toLocaleString('en-PK')}`;
  footerCell.font = { italic: true, color: { argb: 'FF888888' }, size: 9 };
}

// ── Revenue Excel ────────────────────────────────────────────────────────────

export async function exportRevenueExcel(
  { metrics, monthly, byClient, receivables },
  period,
  userName = 'System'
) {
  const wb = new ExcelJS.Workbook();
  wb.creator = BRAND.name;
  wb.created = new Date();

  // Sheet 1 — Summary
  const summary = wb.addWorksheet('Summary');
  applyBrandHeader(summary, 'Revenue Report', period);
  applyHeaderRow(summary, ['Metric', 'Value'], 4);
  const kpis = [
    ['MRR (Current Month)', metrics?.mrrCurrent ?? 0],
    ['ARR (Projected)', metrics?.arrProjected ?? 0],
    ['YTD Revenue', metrics?.totalRevenueYTD ?? 0],
    ['Collection Rate', (metrics?.collectionRate ?? 0) / 100],
    ['Avg Invoice Value', metrics?.avgInvoiceValue ?? 0],
    ['Avg Payment Days', metrics?.avgPaymentDays ?? 0],
  ];
  kpis.forEach(([label, val], i) => {
    const row = summary.getRow(5 + i);
    row.getCell(1).value = label;
    const valCell = row.getCell(2);
    valCell.value = val;
    if (typeof val === 'number' && val < 1 && label.includes('Rate')) {
      valCell.numFmt = PCT_FMT;
    } else if (typeof val === 'number' && label !== 'Avg Payment Days') {
      valCell.numFmt = PKR_FMT;
    }
    row.getCell(1).font = { bold: i === 0 };
  });
  summary.views = [{ state: 'frozen', ySplit: 4 }];
  autoFitColumns(summary);
  addFooter(summary, userName);

  // Sheet 2 — Monthly Breakdown
  const monthlyWs = wb.addWorksheet('Monthly Breakdown');
  applyBrandHeader(monthlyWs, 'Monthly Revenue', period);
  applyHeaderRow(monthlyWs, ['Month', 'Billed', 'Collected', 'Outstanding', 'Growth %'], 4);
  (monthly ?? []).forEach((m, i) => {
    const row = monthlyWs.getRow(5 + i);
    row.getCell(1).value = m.month;
    const billedCell = row.getCell(2);
    billedCell.value = m.billed;
    billedCell.numFmt = PKR_FMT;
    const collCell = row.getCell(3);
    collCell.value = m.collected;
    collCell.numFmt = PKR_FMT;
    const outCell = row.getCell(4);
    outCell.value = m.outstanding;
    outCell.numFmt = PKR_FMT;
    const growthCell = row.getCell(5);
    if (m.growthPercent !== undefined && m.growthPercent !== null) {
      growthCell.value = m.growthPercent / 100;
      growthCell.numFmt = PCT_FMT;
      growthCell.fill = {
        type: 'pattern',
        pattern: 'solid',
        fgColor: { argb: m.growthPercent >= 0 ? 'FFD4EDDA' : 'FFF8D7DA' },
      };
    }
  });
  // Totals row
  const totRow = monthlyWs.getRow(5 + (monthly?.length ?? 0));
  totRow.getCell(1).value = 'TOTAL';
  totRow.getCell(1).font = { bold: true };
  totRow.getCell(2).value = (monthly ?? []).reduce((s, m) => s + m.billed, 0);
  totRow.getCell(2).numFmt = PKR_FMT;
  totRow.getCell(2).font = { bold: true };
  totRow.getCell(3).value = (monthly ?? []).reduce((s, m) => s + m.collected, 0);
  totRow.getCell(3).numFmt = PKR_FMT;
  totRow.getCell(3).font = { bold: true };
  totRow.eachCell((cell) => {
    cell.border = { top: { style: 'thin' } };
  });
  monthlyWs.views = [{ state: 'frozen', ySplit: 4 }];
  autoFitColumns(monthlyWs);
  addFooter(monthlyWs, userName);

  // Sheet 3 — By Client
  const clientWs = wb.addWorksheet('By Client');
  applyBrandHeader(clientWs, 'Revenue by Client', period);
  applyHeaderRow(
    clientWs,
    ['Rank', 'Client', 'Total Billed', 'Collected', 'Outstanding', 'Invoices', 'Projects'],
    4
  );
  (byClient ?? []).slice(0, 20).forEach((c, i) => {
    const row = clientWs.getRow(5 + i);
    row.getCell(1).value = i + 1;
    row.getCell(2).value = c.companyName ?? 'Unknown';
    const b = row.getCell(3);
    b.value = c.totalBilled;
    b.numFmt = PKR_FMT;
    const co = row.getCell(4);
    co.value = c.totalCollected;
    co.numFmt = PKR_FMT;
    const out = row.getCell(5);
    out.value = Math.max(0, c.totalBilled - c.totalCollected);
    out.numFmt = PKR_FMT;
    row.getCell(6).value = c.invoiceCount;
    row.getCell(7).value = c.projectCount;
  });
  clientWs.views = [{ state: 'frozen', ySplit: 4 }];
  autoFitColumns(clientWs);
  addFooter(clientWs, userName);

  // Sheet 4 — Receivables
  const recvWs = wb.addWorksheet('Receivables');
  applyBrandHeader(recvWs, 'Aged Receivables', period);
  applyHeaderRow(recvWs, ['Bucket', 'Invoice Count', 'Total Outstanding'], 4);
  const buckets = [
    ['Current (not overdue)', receivables?.current],
    ['1–30 days overdue', receivables?.days_30],
    ['31–60 days overdue', receivables?.days_60],
    ['61–90 days overdue', receivables?.days_90],
    ['90+ days overdue', receivables?.days_90plus],
  ];
  buckets.forEach(([label, data], i) => {
    const row = recvWs.getRow(5 + i);
    row.getCell(1).value = label;
    row.getCell(2).value = data?.count ?? 0;
    const amtCell = row.getCell(3);
    amtCell.value = data?.totalAmount ?? 0;
    amtCell.numFmt = PKR_FMT;
    if (label.includes('90+')) {
      [1, 2, 3].forEach((col) => {
        row.getCell(col).fill = {
          type: 'pattern',
          pattern: 'solid',
          fgColor: { argb: 'FFF8D7DA' },
        };
      });
    }
  });
  recvWs.views = [{ state: 'frozen', ySplit: 4 }];
  autoFitColumns(recvWs);
  addFooter(recvWs, userName);

  return wb.xlsx.writeBuffer();
}

