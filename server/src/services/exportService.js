// ── PDF helpers ──────────────────────────────────────────────────────────────

function pdfCoverPage(doc, reportTitle, period, generatedBy) {
  doc.rect(0, 0, doc.page.width, 200).fill(BRAND.primaryRgb);
  doc.fillColor('white').fontSize(28).font('Helvetica-Bold').text(BRAND.name, 50, 60);
  doc.fontSize(16).font('Helvetica').text(reportTitle, 50, 100);
  doc.fontSize(12).text(period, 50, 125);
  doc
    .fillColor('#333333')
    .fontSize(10)
    .font('Helvetica')
    .text(`Generated by: ${generatedBy}`, 50, 220);
  doc.text(`Generated at: ${new Date().toLocaleString('en-PK')}`, 50, 235);
}

function pdfSectionHeader(doc, title) {
  doc.moveDown(1);
  doc.rect(doc.x, doc.y, doc.page.width - 100, 22).fill(BRAND.primaryRgb);
  doc
    .fillColor('white')
    .fontSize(12)
    .font('Helvetica-Bold')
    .text(title, doc.x + 5, doc.y - 18);
  doc.fillColor('#333333').font('Helvetica').fontSize(10);
  doc.moveDown(0.5);
}

function pdfTable(doc, headers, rows, opts = {}) {
  const colWidths = opts.colWidths ?? headers.map(() => (doc.page.width - 100) / headers.length);
  const startX = doc.x;
  let y = doc.y;

  // Header
  doc.font('Helvetica-Bold').fontSize(9);
  headers.forEach((h, i) => {
    doc
      .rect(startX + colWidths.slice(0, i).reduce((s, w) => s + w, 0), y, colWidths[i], 18)
      .fill([220, 220, 240]);
    doc
      .fillColor('#333333')
      .text(h, startX + colWidths.slice(0, i).reduce((s, w) => s + w, 0) + 2, y + 4, {
        width: colWidths[i] - 4,
        ellipsis: true,
      });
  });
  y += 20;

  // Rows
  doc.font('Helvetica').fontSize(9);
  rows.forEach((row, ri) => {
    if (y > doc.page.height - 80) {
      doc.addPage();
      y = 50;
    }
    const bg = ri % 2 === 0 ? [255, 255, 255] : [248, 248, 252];
    doc
      .rect(
        startX,
        y,
        colWidths.reduce((s, w) => s + w, 0),
        16
      )
      .fill(bg);
    row.forEach((cell, i) => {
      doc
        .fillColor('#333333')
        .text(
          String(cell ?? ''),
          startX + colWidths.slice(0, i).reduce((s, w) => s + w, 0) + 2,
          y + 3,
          { width: colWidths[i] - 4, ellipsis: true }
        );
    });
    y += 18;
  });
  doc.y = y;
}

function pkr(n) {
  return `PKR ${Number(n ?? 0).toLocaleString('en-PK', { minimumFractionDigits: 2 })}`;
}

function pct(n) {
  return n !== null && n !== undefined ? `${Number(n).toFixed(2)}%` : 'N/A';
}

// ── Invoice PDF ──────────────────────────────────────────────────────────────

/**
 * Render a single invoice to a branded PDF buffer.
 * Expects a populated invoice (client + project) as a plain object.
 */
export function exportInvoicePdf(invoice, generatedBy = 'System') {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ margin: 50, size: 'A4' });
    const chunks = [];
    doc.on('data', (c) => chunks.push(c));
    doc.on('end', () => resolve(Buffer.concat(chunks)));
    doc.on('error', reject);

    const currency = invoice.currency || 'PKR';
    const money = (n) =>
      `${currency} ${Number(n ?? 0).toLocaleString('en-PK', { minimumFractionDigits: 2 })}`;
    const date = (d) => (d ? new Date(d).toLocaleDateString('en-PK') : '—');

    const client = invoice.client || {};
    const primaryContact =
      (client.contacts || []).find((c) => c.isPrimary) || (client.contacts || [])[0] || {};

    // Header band — dark so the white Verixsoft logo reads cleanly.
    doc.rect(0, 0, doc.page.width, 120).fill(BRAND.primaryRgb);

    // Logo (left) + company name beside it. Logo is best-effort: if the asset
    // is missing we fall back to a text-only lockup rather than failing.
    let nameX = 50;
    try {
      doc.image(BRAND.logoPath, 50, 35, { width: 50, height: 50 });
      nameX = 112;
    } catch {
      // no logo asset — keep the name flush-left
    }
    doc.fillColor('white').fontSize(24).font('Helvetica-Bold').text(BRAND.name, nameX, 42);
    doc
      .fontSize(10)
      .font('Helvetica')
      .fillColor('#C7D0E0')
      .text(`${BRAND.tagline} · ${BRAND.address}`, nameX, 74);

    doc
      .fontSize(28)
      .font('Helvetica-Bold')
      .fillColor('white')
      .text('INVOICE', 0, 42, { align: 'right', width: doc.page.width - 50 });
    doc
      .fontSize(11)
      .font('Helvetica')
      .text(`#${invoice.invoiceNumber}`, 0, 78, { align: 'right', width: doc.page.width - 50 });

    doc.fillColor('#333333');

    // Meta: bill-to + dates
    let y = 150;
    doc.font('Helvetica-Bold').fontSize(10).text('BILL TO', 50, y);
    doc.font('Helvetica').fontSize(11);
    doc.text(client.companyName || 'Client', 50, y + 16);
    if (primaryContact.name) doc.text(primaryContact.name, 50, y + 32);
    if (primaryContact.email) doc.text(primaryContact.email, 50, y + 48);

    const rightX = 330;
    doc.font('Helvetica-Bold').fontSize(10).text('DETAILS', rightX, y);
    doc.font('Helvetica').fontSize(10);
    doc.text(`Status: ${String(invoice.status || 'draft').toUpperCase()}`, rightX, y + 16);
    doc.text(`Issued: ${date(invoice.sentAt || invoice.createdAt)}`, rightX, y + 32);
    doc.text(`Due: ${date(invoice.dueDate)}`, rightX, y + 48);
    if (invoice.project?.name) doc.text(`Project: ${invoice.project.name}`, rightX, y + 64);

    // Line items table
    doc.y = y + 96;
    doc.x = 50;
    pdfTable(
      doc,
      ['Description', 'Qty', 'Unit Price', 'Amount'],
      (invoice.lineItems || []).map((it) => [
        it.description,
        it.quantity,
        money(it.unitPrice),
        money((it.quantity || 0) * (it.unitPrice || 0)),
      ]),
      { colWidths: [250, 50, 100, 100] }
    );

    // Totals
    doc.moveDown(1);
    const totals = [
      ['Subtotal', money(invoice.subtotal)],
      ...(invoice.discountAmount
        ? [[`Discount (${invoice.discountPercent || 0}%)`, `- ${money(invoice.discountAmount)}`]]
        : []),
      ...(invoice.taxAmount
        ? [[`Tax (${invoice.taxPercent || 0}%)`, money(invoice.taxAmount)]]
        : []),
      ['Total', money(invoice.total)],
      ['Paid', money(invoice.paidAmount)],
      ['Amount Due', money(Math.max(0, (invoice.total || 0) - (invoice.paidAmount || 0)))],
    ];
    const tx = 330;
    let ty = doc.y;
    totals.forEach(([label, val]) => {
      const isTotal = label === 'Total' || label === 'Amount Due';
      doc.font(isTotal ? 'Helvetica-Bold' : 'Helvetica').fontSize(isTotal ? 12 : 10);
      doc.fillColor('#333333').text(label, tx, ty, { width: 110 });
      doc.text(val, tx + 110, ty, { width: 105, align: 'right' });
      ty += isTotal ? 20 : 16;
    });

    if (invoice.notes) {
      doc.moveDown(2);
      doc
        .font('Helvetica-Bold')
        .fontSize(10)
        .text('Notes', 50, ty + 16);
      doc
        .font('Helvetica')
        .fontSize(10)
        .fillColor('#555555')
        .text(invoice.notes, 50, ty + 32, {
          width: doc.page.width - 100,
        });
    }

    // Footer pinned to the bottom of the current page. Temporarily drop the
    // bottom margin so PDFKit doesn't spill the footer onto a fresh page.
    const savedBottom = doc.page.margins.bottom;
    doc.page.margins.bottom = 0;
    doc
      .fontSize(9)
      .fillColor('#888888')
      .font('Helvetica-Oblique')
      .text(
        `${BRAND.name} · ${BRAND.tagline}  —  Generated by ${generatedBy} on ${new Date().toLocaleString('en-PK')}`,
        50,
        doc.page.height - 50,
        { align: 'center', width: doc.page.width - 100, lineBreak: false }
      );
    doc.page.margins.bottom = savedBottom;

    doc.end();
  });
}

